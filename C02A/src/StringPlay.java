import java.awt.Point;

public class StringPlay {
	public static void main(String[] args) {
		String string1 = "C++ is cool";
		String string2 = "I love Java";
		System.out.println(string1);
		System.out.println(string2);
		
		String string3 = string2.substring(7, 11) + string1.substring(3, 11);
		System.out.println(string3);
		
		
		Point point1 = new Point(1, 1);
		System.out.println("\nPoint 1: " + point1);
		Point point2 = new Point(1, 1);
		System.out.println("Point 2: " + point2);
		System.out.println("Equality test using ==: " + (point1 == point2));
		System.out.println("Equality test using equals(): " + (point1.equals(point2)));
	
		String string4 = "helluva";
		String string5 = "HELLUVA";
		String string6 = "HeLlUvA";
		
		System.out.println("\nequals() Test");
		System.out.println(string4 + " = " + string5 + ": " + string4.equals(string5));
		System.out.println(string4 + " = " + string6 + ": " + string4.equals(string6));
		System.out.println(string5 + " = " + string6 + ": " + string5.equals(string6));
		
		System.out.println("\nequalsIgnoreCase() Test");
		System.out.println(string4 + " = " + string5 + ": " + string4.equalsIgnoreCase(string5));
		System.out.println(string4 + " = " + string6 + ": " + string4.equalsIgnoreCase(string6));
		System.out.println(string5 + " = " + string6 + ": " + string5.equalsIgnoreCase(string6));
		
	}
}
